def set_bit(w, k):
    return w | (1 << k)


def clear_bit(w, k):
    return w & ~(1 << k)


def toggle_bit(w, k):
    return w ^ (1 << k)


def rotate(x, b, direction, n=64):
    if direction == LEFT:
        return (x << b) | (x >> (n - b))

    if direction == RIGHT:
        return (x >> b) | (x << (n - b))

